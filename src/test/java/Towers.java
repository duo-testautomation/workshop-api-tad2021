public class Towers {
    int aKerk;
    int academieGebouw;
    int martini;
    int nieuweKerk;
    int stJozefKerk;

    public Towers(int aKerk, int academieGebouw, int martini, int nieuweKerk, int stJozefKerk) {
        this.aKerk = aKerk;
        this.academieGebouw = academieGebouw;
        this.martini = martini;
        this.nieuweKerk = nieuweKerk;
        this.stJozefKerk = stJozefKerk;
    }

    public int getaKerk() {
        return aKerk;
    }

    public void setaKerk(int aKerk) {
        this.aKerk = aKerk;
    }

    public int getAcademieGebouw() {
        return academieGebouw;
    }

    public void setAcademieGebouw(int academieGebouw) {
        this.academieGebouw = academieGebouw;
    }

    public int getMartini() {
        return martini;
    }

    public void setMartini(int martini) {
        this.martini = martini;
    }

    public int getNieuweKerk() {
        return nieuweKerk;
    }

    public void setNieuweKerk(int nieuweKerk) {
        this.nieuweKerk = nieuweKerk;
    }

    public int getStJozefKerk() {
        return stJozefKerk;
    }

    public void setStJozefKerk(int stJozefKerk) {
        this.stJozefKerk = stJozefKerk;
    }
}
